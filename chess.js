  var f0 = e0.length === 2 ? pen.lineTo : pen.bezTo;
  var f1 = e1.length === 2 ? pen.lineTo : pen.bezTo;
  var f2 = e2.length === 2 ? pen.lineTo : pen.bezTo;
  var f3 = e3.length === 2 ? pen.lineTo : pen.bezTo;
  corners.push([e0[0],e1[0],e2[0],e3[0]]);
  e0 = e0.slice(1);
  e1 = e1.slice(1);
  e2 = e2.slice(1);
